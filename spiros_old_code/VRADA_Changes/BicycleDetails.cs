public class BicycleDetails
{
    public float Speed;
    public float RPM;
    public int Distance;
    public float Calories;
    public float HF;
    public int Power;
    public int Level;

    public BicycleDetails(float Speed, float RPM, int Distance, float Calories, float HF, int Power, int Level)
    {
        this.Speed = Speed;
        this.RPM = RPM;
        this.Distance = Distance;
        this.Calories = Calories;
        this.HF = HF;
        this.Power = Power;
        this.Level = Level;

    }
}